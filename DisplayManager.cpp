void DisplayManager::animFullscreenNoteIn(int16_t noteScreenIndex){
  fullscreenNoteNumber =  nm->getNoteIndexAtScreenPosition(noteScreenIndex);
  //Prep: Take screenshot, prepare lil text buffer
  prepareNoteField(fullscreenNoteNumber, false); // Prepare the note without crossing it out
  uint16_t elementsFromX = SCREEN_WIDTH - noteField.width();
  uint16_t elementsFromY = HEADER_HEIGHT + 1 + noteScreenIndex * NOTE_HEIGHT;
  uint16_t elementsFromWidth = noteField.width();
  uint16_t elementsFromHeight = noteField.height();
  uint16_t frameToX = 2;
  uint16_t frameToY = HEADER_HEIGHT + 4;
  uint16_t frameToWidth = SCREEN_WIDTH - 2 * frameToX;
  uint16_t frameToHeight = SCREEN_HEIGHT - HEADER_HEIGHT - 4 - 2;
  uint16_t innerToX = frameToX + 2;
  uint16_t innerToY = frameToY + 2;
  uint16_t innerToWidth = frameToWidth - 4;
  uint16_t innerToHeight = frameToHeight - 4;
  // Three elements - frame, inner, text
  // In progress, anim elements from innersquare, to wanted x/y width/height
  
  unsigned long startTime = millis();
  unsigned long endTime = startTime + SCREEN_SWITCH_TIME;
  while (millis() <= endTime) {
    unsigned long deltaTime = millis() - startTime;
    float animProgress = ((float)deltaTime) / ((float)SCREEN_SWITCH_TIME);
    float animProgressEased = easeInOut(animProgress);
    if (animProgressEased>1){
      animProgressEased = 1;
    }

    uint16_t frameX = animProgressEased * ((float)(frameToX - elementsFromX)) + elementsFromX;
    uint16_t frameY = animProgressEased * ((float)(frameToY - elementsFromY)) + elementsFromY;
    uint16_t frameWidth = animProgressEased * ((float)(frameToWidth - elementsFromWidth)) + elementsFromWidth;
    uint16_t frameHeight = animProgressEased * ((float)(frameToHeight - elementsFromHeight)) + elementsFromHeight;

    display.fillRect(frameX, frameY, frameWidth, frameHeight, BLACK);

    uint16_t innerX = animProgressEased * ((float)(innerToX - elementsFromX)) + elementsFromX;
    uint16_t innerY = animProgressEased * ((float)(innerToY - elementsFromY)) + elementsFromY;
    uint16_t innerWidth = animProgressEased * ((float)(innerToWidth - elementsFromWidth)) + elementsFromWidth;
    uint16_t innerHeight = animProgressEased * ((float)(innerToHeight - elementsFromHeight)) + elementsFromHeight;
    display.fillRect(innerX, innerY, innerWidth, innerHeight, WHITE);

    display.drawBitmap(innerX, innerY, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);

    display.refresh();
  }
  drawFullscreenNote();
  display.refresh();
}

