bool DisplayManager::indexIsOnScreen(int16_t noteIndex) {
  int16_t screenPos =  noteIndex - nm->getNoteIndex();
  return 0 <= screenPos && screenPos <= 5;
}

void DisplayManager::openSpaceAt(int16_t noteIndex) {
  if (!indexIsOnScreen(noteIndex)) {
    ESP_LOGD(TAG, "Tried to open space outside screen.");
    return;  // Outside screen, no reason to animate
  }

  int16_t screenPos = noteIndex - nm->getNoteIndex();
  ESP_LOGD(TAG, "Opening space before note %d at screen position %d.", noteIndex, screenPos);
  prepareScreenElement(nm->getNoteIndex()+6);
  scrollToNoteIndex(nm->getNoteIndex()-1, 1, noteIndex);
}

void DisplayManager::closeSpaceAt(int16_t noteIndex) {
  if (!indexIsOnScreen(noteIndex)) {
    ESP_LOGD(TAG, "Tried to close note outside screen.");
    return;  // Outside screen, no reason to animate
  }

  int16_t screenPos = noteIndex - nm->getNoteIndex();
  ESP_LOGD(TAG, "Collapsing note %d at screen position %d.", noteIndex, screenPos);
  prepareScreenElement(nm->getNoteIndex()+6);
  scrollToNoteIndex(nm->getNoteIndex()+1, 6, noteIndex);
}

void DisplayManager::prepareNoteField(int16_t noteNumber, bool crossIfChecked) {
  // Only redraw if different from previously prepared as this is quite expensive.
  static int16_t lastNoteFieldPreparedIndex = -1;
  if (noteNumber == lastNoteFieldPreparedIndex && !crossIfChecked){
    return;
  }
  lastNoteFieldPreparedIndex = noteNumber;

  ESP_LOGV(TAG, "Preparing note field %d.", noteNumber);
  noteField.fillScreen(0);
  if (noteNumber < nm->getNoteCount() && noteNumber >= 0) {
    noteField.setCursor(0, 2);
    
    String noteText = nm->getNoteText(noteNumber);
    
    uint32_t noteTextLength = noteText.length();
    uint8_t checkLineThickness = getNoteLineThicknessFromLength(noteTextLength);
    uint8_t noteTextSize = getNoteTextSizeFromLength(noteTextLength);
    uint16_t maxTextLength = getMaxTextLengthFromSize(noteTextSize);
    if (noteTextLength > maxTextLength){
      noteText = noteText.substring(0, maxTextLength - 3);
      noteText += "...";
      noteTextLength = noteText.length();
    }
    
    noteField.setTextSize(noteTextSize); 
    noteField.print(noteText);
    
    if (crossIfChecked && nm->getNoteChecked(noteNumber)) {
      uint32_t checkLineLengthToDraw = noteTextLength * BASE_CHARACTER_WIDTH * noteTextSize;
      uint8_t checkLineIndex = 0;

      while(checkLineLengthToDraw>0){
        uint32_t checkLineLength = checkLineLengthToDraw;
        if (checkLineLength >= noteField.width()){
          checkLineLength = noteField.width();
        }
        noteField.fillRect(0, 2 + (BASE_CHARACTER_HEIGHT * noteTextSize * checkLineIndex) + (BASE_CHARACTER_HEIGHT / 2 * noteTextSize) - checkLineThickness/2, checkLineLength, checkLineThickness, WHITE);
        checkLineLengthToDraw -= checkLineLength;
        checkLineIndex += 1;
      }
    }
  }
}

uint8_t DisplayManager::getNoteTextSizeFromLength(uint32_t length){
  if (systemConfig.constTextSize){
    return systemConfig.minTextSize;
  }
  uint8_t targetSize = 1;
  if (length<6){// A single line (of max 6 characters)
    //targetSize = 6;
    targetSize = 3; // 6 too large, temp disabled
  }else if(length < 24){ // Two lines (12 characters wide)
    targetSize = 3;
  }else if(length < 54){ // Three lines (18 characters wide)
    targetSize = 2;
  }else{ // Smallest possible (36 char wide, 6 lines, 216 chars)
    targetSize = 1;
  }
  return (max( targetSize, systemConfig.minTextSize));
}

