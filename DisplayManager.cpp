void DisplayManager::animCrossNote(int16_t noteScreenIndex, float animProgress){
  int16_t noteNumber =  nm->getNoteIndexAtScreenPosition(noteScreenIndex);
  // Clamp progress
  if (animProgress < 0.0){
    animProgress = 0.0;
  }
  if (animProgress > 1.0){
    animProgress = 1.0;
  }

  animProgress = easeIn(animProgress); // Add easing to accelerate the animation.

  // If the note is currently checked, we reverse the process
  if (nm->getNoteChecked(noteNumber)){
    animProgress = 1.0 - animProgress;
  }

  prepareNoteField(noteNumber, false); // Prepare the note without crossing it out

  // Prepare note field only if not already prepared
  static int16_t lastNoteNumber = -1;
  static uint32_t noteTextLength;
  static uint8_t noteTextSize;
  static uint8_t checkLineThickness;
  if (lastNoteNumber != noteNumber){
    noteTextLength = nm->getNoteText(noteNumber).length();
    noteTextSize = getNoteTextSizeFromLength(noteTextLength);
    checkLineThickness = getNoteLineThicknessFromLength(noteTextLength);
    lastNoteNumber = noteNumber;
    noteTextLength = min(noteTextLength, (uint32_t)getMaxTextLengthFromSize(noteTextSize));
  }

  // Copy note field to position
  display.drawBitmap(SCREEN_WIDTH - noteField.width(), HEADER_HEIGHT + 1 + noteScreenIndex * NOTE_HEIGHT, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);
  
  // Draw partial checked line on relative note offset
  uint32_t checkLineLengthToDraw = (uint32_t)((float)(noteTextLength * BASE_CHARACTER_WIDTH * noteTextSize)* animProgress);
  uint8_t checkLineIndex = 0;

  ESP_LOGD(TAG, "Animating checking of note %d at progress %f, giving a total line length of %d", noteNumber, animProgress, checkLineLengthToDraw);

  while(checkLineLengthToDraw>0){
    uint32_t checkLineLength = checkLineLengthToDraw;
    if (checkLineLength >= noteField.width()){
      checkLineLength = noteField.width();
    }
    display.fillRect(SCREEN_WIDTH - noteField.width(), (HEADER_HEIGHT + 1 + noteScreenIndex * NOTE_HEIGHT) + 2 + (BASE_CHARACTER_HEIGHT * noteTextSize * checkLineIndex) + (BASE_CHARACTER_HEIGHT / 2 * noteTextSize) - checkLineThickness/2, checkLineLength, checkLineThickness, BLACK);
    checkLineLengthToDraw -= checkLineLength;
    checkLineIndex += 1;
  }
  display.refresh();
}

void DisplayManager::prepareScreenElement(int16_t noteNumber) {
  ESP_LOGV(TAG, "Preparing screen element %d.", noteNumber);
  screenElement.fillScreen(WHITE);
  screenElement.drawFastHLine(0, 0, 240, 0);
  screenElement.drawFastVLine(20, 0, screenElement.height(), 0);
  if (noteNumber < nm->getNoteCount() && noteNumber >= 0) {
    if (nm->getNoteChecked(noteNumber)) {
      screenElement.fillCircle(10, screenElement.height() / 2, 5, BLACK);
    } else {
      screenElement.drawCircle(10, screenElement.height() / 2, 5, BLACK);
    }
    prepareNoteField(noteNumber);
    screenElement.drawBitmap(screenElement.width() - noteField.width(), 1, noteField.getBuffer(),
                             noteField.width(), noteField.height(), BLACK, WHITE);
  }
}

int16_t DisplayManager::scrollToNoteIndex(int16_t reqIndex, int8_t allowedPastBorders, int16_t freezeIndex) {
  int16_t toIndex = reqIndex;
  if (toIndex < 0) {
    // If we're going above the list, limit to allowed distance
    if (toIndex < -allowedPastBorders) {
      toIndex = -allowedPastBorders;
    }
  } else {
    if (nm->getNoteCount() + allowedPastBorders <= 6) {
      // If screen is not full, don't move.
      ESP_LOGW(TAG, "Screen not full, no scroll");
      return nm->getNoteIndex();
    } else {
      // Never create more space than allowed past borders
      if (toIndex + 6 > nm->getNoteCount() + allowedPastBorders) {
        toIndex = nm->getNoteCount() + allowedPastBorders - 6;
      }
    }
  }
  if (toIndex != reqIndex) {
    ESP_LOGD(TAG, "Adjusted scroll index from %d to %d.", reqIndex, toIndex);
  }

  ESP_LOGD(TAG, "Scrolling to index %d, while freezing notes at index %d.", toIndex, freezeIndex);
  int deltaIndex = toIndex - nm->getNoteIndex();  // How many notes are we scrolling
  if (deltaIndex == 0) {
    return toIndex;
  }

  unsigned long startTime = millis();
  unsigned long scrollTime = SCROLL_TIME_BASE + SCROLL_TIME_BASE / 4 * abs(deltaIndex);  // Dependent on number of notes
  unsigned long endTime = startTime + scrollTime;

  int noteScrollIndexPrev = -1;  // What note is in buffer?

  unsigned int scrollDistance = abs(deltaIndex) * NOTE_HEIGHT;  // How far are we going (Should be a multiple of 8)
  unsigned int targetScrollPos = 0;                             // Ideally how many pixels offset right now
  unsigned int currentScrollPos = 0;                            // Practically how many pixels offset right now (has to be multiple of 8)
  uint16_t scrollStartHeight = HEADER_HEIGHT;

  int16_t freezeScreenIndex = freezeIndex - nm->getNoteIndex();
  if(freezeScreenIndex > 0){
    scrollStartHeight += freezeScreenIndex* NOTE_HEIGHT;
    ESP_LOGD(TAG, "Freezing at index %d gives relative index %d, scrolling starts at %d pixels", freezeIndex, freezeScreenIndex, scrollStartHeight);
  }

  while (currentScrollPos < scrollDistance) {  // In the end, what is on screen should have travelled the full scroll disatance
    // Calculate where abouts on the easing curve we are
    unsigned long deltaTime = millis() - startTime;                       // Time since we began
    float animProgressLinear = ((float)deltaTime) / ((float)scrollTime);  // Linear decimal progress
    float animProgressEased = easeInOut(animProgressLinear);              // Eased decimal progress
    targetScrollPos = (unsigned int)ceil(animProgressEased * (float)scrollDistance);
    if (animProgressEased > 1) {
      targetScrollPos = scrollDistance;  // Never let progress exceed scrollDistance
    }
    while (currentScrollPos + 8 <= targetScrollPos) {  // If we have the space to increment by 8, and move current pos along

      // Figure out what note and what segment to scroll in
      int noteScrollPos = (currentScrollPos) % NOTE_HEIGHT;  // What fraction are we scrolling in
      int noteScrollIndex;                                   // What note are we scrolling in? Depends on if at bottom or top
      if (deltaIndex > 0) {
        noteScrollIndex = nm->getNoteIndex() + 6 + (currentScrollPos) / NOTE_HEIGHT;
      } else {
        noteScrollIndex = nm->getNoteIndex() - 1 - (currentScrollPos) / NOTE_HEIGHT;
      }

      // If we're switching to a new note, load it in
      if (noteScrollIndex != noteScrollIndexPrev) {
        noteScrollIndexPrev = noteScrollIndex;
        if (freezeScreenIndex < 0 || deltaIndex > 0){
          prepareScreenElement(noteScrollIndex);
        }else{
          prepareScreenElement(nm->getNoteCount());
        }
      }

      // Setting up source and destination locations for moving buffer data around
      int displaySource, displayDestination, noteSource, noteDestination;
      if (deltaIndex > 0) {
        displaySource = 0;
        displayDestination = 1;
        noteDestination = 0;
        noteSource = (NOTE_HEIGHT - 1 - noteScrollPos) / 8;
      } else {
        displaySource = 1;
        displayDestination = 0;
        noteDestination = (SCREEN_HEIGHT - scrollStartHeight) / 8 - 1;
        noteSource = (noteScrollPos) / 8;
      }
      // Move lines, fill in bytes at the end
      for (int j = 0; j < SCREEN_WIDTH; j++) {
        // Note screen is rotated -90deg, hence header is start of screen
        // Note pixels are stored 8 in a byte, moves can only occur in offsets of 8
        // Move each line one byte upwards (or downwards
        memmove(
          display.getBuffer() + j * SCREEN_HEIGHT / 8 + displayDestination,  // Destination
          display.getBuffer() + j * SCREEN_HEIGHT / 8 + displaySource,       //Source
          SCREEN_HEIGHT / 8 - scrollStartHeight / 8 - 1);                        // Length
        // Move in line from buffer to now empty line under (or over) scrolled screen.
        display.getBuffer()[j * SCREEN_HEIGHT / 8 + noteDestination] = reverseByte(screenElement.getBuffer()[j * NOTE_HEIGHT / 8 + noteSource]);
      }
      currentScrollPos += 8;
    }
    // Current frame done refresh
    display.refresh();
  }
  for (int i = 0; i<6; i++){
    drawNoteWidget(i + toIndex, toIndex);
  }
  display.refresh();
  return toIndex;
  //refreshNotes();
}

void DisplayManager::clearToLargeIcon(uint8_t icon, bool animate) {
  if (animate){
  unsigned long startTime = millis();
  unsigned long endTime = startTime + SLIDE_TIME*2;
  while (millis() < endTime) {
    unsigned long deltaTime = millis() - startTime;
    float animProgress = ((float)deltaTime) / ((float)SLIDE_TIME*2);
    float animProgressEased = easeInOut(animProgress);
    unsigned int radius = (unsigned int)ceil(animProgressEased * (float)(234)); //Hypotenuse
    display.fillCircle(SCREEN_WIDTH/2, SCREEN_HEIGHT/2, radius, WHITE);
    if (radius>60){ //Hypotenuse, and a bit
      drawLargeIcon(icon);
    }
    display.refresh();
  }
  }
  // Clear everything for sure
  display.fillScreen(WHITE);
  drawLargeIcon(icon);
  display.refresh();
}

