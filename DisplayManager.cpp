void DisplayManager::revealDisplay() {
  ESP_LOGD(TAG, "Revealing display.");
  drawBaseLayout();
  display.refresh();

  for (int i = nm->getNoteIndex(); i < min((int16_t)(nm->getNoteIndex() + 6), nm->getNoteCount()); i++) {
    slideInNote(i);
    drawNoteWidget(i);
    display.refresh();
  }
}

void DisplayManager::redrawDisplay(bool shouldRefresh) {
  ESP_LOGV(TAG, "Redrawing entire display.");
  drawBaseLayout();
  for (int i = nm->getNoteIndex(); i < nm->getNoteIndex() + 6; i++) {
    prepareNoteField(i);
    display.drawBitmap(SCREEN_WIDTH - noteField.width(), HEADER_HEIGHT + 1 + (i - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);
    drawNoteWidget(i);
  }
  if (shouldRefresh){
    if (isFullscreenNote()){
      drawFullscreenNote();
    }
    display.refresh();
  }
}

void DisplayManager::drawNoteWidget(int16_t noteNumber, int16_t screenPosition) {
  ESP_LOGV(TAG, "Drawing widget for note %d.", noteNumber);
  int16_t screenIndex;
  if (screenPosition == -1){
     screenIndex = noteNumber - nm->getNoteIndex();
  }else{
    screenIndex = noteNumber - screenPosition;
  }
  if (screenIndex<0 || screenIndex>5){return;} // Don't draw widgets that don't exist
  display.fillRect(0, HEADER_HEIGHT + 1 + screenIndex * NOTE_HEIGHT, 20, NOTE_HEIGHT - 2, WHITE);
  int16_t midPosX = 10;
  int16_t midPosY = HEADER_HEIGHT + 1 + screenIndex * NOTE_HEIGHT + NOTE_HEIGHT / 2;
  if(screenIndex == 0 && noteNumber != 0){
    // Notes above, can scroll up, arrow up
    display.fillTriangle(midPosX-4, midPosY+4, midPosX+4, midPosY+4, midPosX, midPosY-4, BLACK);
  }else if(screenIndex == 5 && noteNumber<(nm->getNoteCount()-1)){
    // Notes below, can scroll down, arrow down
    display.fillTriangle(midPosX-4, midPosY-4, midPosX+4, midPosY-4, midPosX, midPosY+4, BLACK);
  }
  else if (noteNumber < nm->getNoteCount()) {
    if (nm->getNoteChecked(noteNumber)) {
      display.fillCircle(midPosX, midPosY, 5, 0);
    } else {
      display.drawCircle(midPosX, midPosY, 5, 0);
    }
  }
}

void DisplayManager::slideInNote(int16_t noteNo) {
  if (!indexIsOnScreen(noteNo)) {
    ESP_LOGD(TAG, "Tried to slide in note outside screen.");
    return;  // Outside screen, no reason to animate
  }

  ESP_LOGD(TAG, "Sliding in note %d.", noteNo);
  unsigned long startTime = millis();
  unsigned long endTime = startTime + SLIDE_TIME;
  prepareNoteField(noteNo);
  while (millis() < endTime) {
    unsigned long deltaTime = millis() - startTime;
    float animProgress = ((float)deltaTime) / ((float)SLIDE_TIME);
    float animProgressEased = easeOut(animProgress);
    unsigned int notePosOffset = (unsigned int)ceil(animProgressEased * (float)noteField.width());
    display.drawBitmap(SCREEN_WIDTH - notePosOffset, HEADER_HEIGHT + 1 + (noteNo - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);
    display.refresh();
  }
  display.drawBitmap(SCREEN_WIDTH - noteField.width(), HEADER_HEIGHT + 1 + (noteNo - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);
  display.refresh();
}

void DisplayManager::slideOutNote(int16_t noteNo) {
  if (!indexIsOnScreen(noteNo)) {
    ESP_LOGD(TAG, "Tried to slide out note outside screen.");
    return;  // Outside screen, no reason to animate
  }

  ESP_LOGD(TAG, "Sliding out note %d.", noteNo);
  unsigned long startTime = millis();
  unsigned long endTime = startTime + SLIDE_TIME;
  prepareNoteField(noteNo);
  while (millis() < endTime) {
    unsigned long deltaTime = millis() - startTime;
    float animProgress = ((float)deltaTime) / ((float)SLIDE_TIME);
    float animProgressEased = easeIn(animProgress);
    unsigned int notePosOffset = (unsigned int)ceil(animProgressEased * (float)noteField.width());
    display.fillRect(SCREEN_WIDTH - noteField.width(), HEADER_HEIGHT + 1 + (noteNo - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.width() - notePosOffset, noteField.height(), WHITE);
    display.drawBitmap(SCREEN_WIDTH - noteField.width() + notePosOffset, HEADER_HEIGHT + 1 + (noteNo - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.getBuffer(), noteField.width(), noteField.height(), BLACK, WHITE);
    display.refresh();
  }
  display.fillRect(SCREEN_WIDTH - noteField.width(), HEADER_HEIGHT + 1 + (noteNo - nm->getNoteIndex()) * NOTE_HEIGHT, noteField.width(), noteField.height(), WHITE);
  display.refresh();
}

